          <h4><u></u></h4>
          <p><b>A. Perkandangan</b></p>
          <p>
            Lokasi kandang sangat mempengaruhi kesehatan dan produktivitas
            puyuh. Lokasi kandang sangat menentukan lancar dan tidaknya
            distribusi dari dan ke kandang, yaitu distribusi bibit, pakan,
            sarana dan hasil produksi. Untuk itu dalam menentukan lokasi kandang
            perlu diperhatikan beberapa hal antara lain, berada cukup jauh dari
            pemukiman penduduk, transportasi relatif mudah, tersedia cukup air
            dan saluran pembuangan, pencahayaan dan sirkulasi udara lancar, aman
            dan mudah pengawasannya (Wuryadi, 2011). Seperti halnya kandang
            ayam, kandang puyuh terdiri dari beberapa macam. Setiap macamnya
            memiliki kelebihan dan kekurangan. Sistem kandang yang biasa
            diterapkan adalah sistem litter dan sangkar atau baterai. Kandang
            sistem baterai mempunyai kelebihan yaitu mudah dibersihkan dan
            sirkulasi udara lancar. Kandang puyuh sistem litter mempunyai
            beberapa keunggulan yaitu menghemat tenaga kerja dan praktis, karena
            tidak perlu dibersihkan setiap hari, dapat memberikan rasa hangat
            pada puyuh, kesehatan kaki puyuh juga terjaga karena tidak langsung
            mengenai lantai yang keras, litter juga memberikan kesibukan dari
            puyuh sehingga dapat mengurang sifat kanibal pada puyuh. Kekurangan
            dari kandang sistem litter diantaranya adalah telur tertutup oleh
            litter, sehingga dapat terinjak oleh puyuh, tempat pakan dan minum
            akan cepat kotor karena tecemar litter yang dikais-kais oleh puyuh.
            Debu yang timbul akibat litter yang dikais-kais oleh puyuh dapat
            menyebabkan penyakit pernafasan Listiyowati dan Roospitasari, 2009).
          </p>
          <p><b>B. Bibit Puyuh</b></p>
          <p>
            Pembudidayaan puyuh untuk memproduksi telur sekaligus daging,
            membutuhkan bibit puyuh yang berkualitas. Bibit puyuh yang akan
            diperoleh sebaiknya dari ras unggul dan diperoleh dari peternak yang
            sudah mempunyai kredibilitas. DOQ yang baik mempunyai ciri-ciri
            antara lain: 
            <p>1. DOQ terlihat lineah, tidak cacat, terutama kaki dan
            paruh.</p> 
            <p>2. DOQ mempunyai bobot dan ukuran yang seragam sekitar 6-8
            gram/ekor.</p> 
            <p>3. Bentuk bulu normal, mengkilap dan tidak kusam.</p> 
            <p>4. DOQ bukan berasal dari perkawinan Inbreeding. 
            <p>Selain dilakukan pemilihan
            bibit pada fase starter, pemilihan bibit juga dilakukan pada fase
            selanjutnya yaitu fase grower dan layer agar didapat puyuh yang
            menghasilkan jumlah telur yang tinggi dan berkualitas, ciri-ciri
            puyuh fase starter dan grower yang baik antara lain:</p> 
            <p>1. Puyuh memiliki badan yang sehat, tidak menunjukkan tanda-tanda sakit dan terlihat lincah.</p> 
            <p>2. Seluruh bagian tubuh lengkap dan tidak cacat.</p> 
            <p>3. Mata bening dan cerah.</p> 
            <p>4. Bentuk kepala, tubuh dan kaki proporsional.</p> 
            <p>5. Bobot badan seragam dan ideal sekitar 150gram/ekor.</p> 
            <p>6. Asal-usul indukan jelas, dan bukan merupakan hasil perkawinan sedarah atau inbreeding.</p>
          </p>
          <p><b>C. Pemeliharaan</b></p>
          <p>
            Pemeliharaan burung puyuh terdiri dari 3 fase yaitu 1) pemeliharaan
            puyuh starter yang dilakukan dikandang khusus puyuh starter,
            pemeliharaan puyuh starter harus terpisah dari puyuh yang lebih
            besar agar tidak terjadi perkelahian. Perkelahian dapat
            mengakibatkan cacat bahkan kematian. 2) pemeliharaan puyuh fase
            grower, pemeliharaan puyuh grower dilakukan dalam kandang grower. 3)
            pemeliharaan puyuh fase layer, pada pemeliharaan puyuh pada fase
            layer kandang yang digunakan sama seperti kandang grower. Kandang
            untuk skala besar sebaiknya tidak dalam ukuran besar sekaligus,
            tetapi berukuran sedang yang disatukan dalam kandang besar, dengan
            demikian pemeliharaan menjadi lebih mudah dan puyuh tidak saling
            berkelahi karena populasi terlalu besar. Luas kandang yang dibuat
            tergantung kebutuhan dan jumlah puyuh yang dipelihara. Untuk kandang
            berukuran 1 m² dapat diisi 90-100 ekor anak puyuh. Sementara untuk
            anak puyuh umur berumur 10 hari hingga lepas anakan per meter
            persegi dapat diisi 60 ekor puyuh, dan selanjutnya menjadi 40 ekor
            per meter persegi sampai dengan puyuh diafkir.
          </p>
          <p><b>D. Pakan</b></p>
          <p>
            Jenis pakan dibedakan menurut bentuknya dan kegunaannya dalam fase
            pemeliharaan puyuh. Menurut bentuknya, pakan dibagi menjadi 3 yaitu,
            1) mash atau pakan yang berbentuk tepung, 2) crumble atau pakan yang
            berbentuk remah- remah, keuntungan pakan bentuk ini mudah dipatuk
            sehingga lebih disukai puyuh, dan 3) pellet, bentuk pelet seperti
            biji-bijian sehingga dapat mengundang selera makan ternak. Sedangkan
            menurut penggunaannya berdasarkan fase pemeliharaan, pakan dapat
            dibedakan menjadi 2 jenis, yaitu 1) pakan fase starter, yaitu pakan
            yang diberikan pada masa pertumbuhan, fase pertumbuhan tersebut
            mulai DOQ masuk sampai siap bertelur. 2) pakan fase layer, yaitu
            pakan yang diberikan pada puyuh yang mulai bertelur.
          </p>
          <p><b>E. Penyakit</b></p>
          <p>
            Pencegahan penyakit pada pemeliharaan puyuh lebih diutamakan
            dibandingkan dengan pengobatan, karena biaya yang dikeluarkan untuk
            pencegahan relatif lebih murah dibandingkan dengan biaya pengobatan.
            Meskipun puyuh termasuk jenis unggas yang tahan terhadap penyakit,
            tidak menutup kemungkinan puyuh masih tetap terjangkit oleh
            penyakit, beberapa jenis penyakit yang menyerang puyuh antara lain
            adalah :
            <p>1. Snot atau Infection Coryza Penyakit snot disebabkan oleh
            bakteri Hemophilus gallinarum. Gejala yang terlihat, diantaranya
            puyuh terlihat lesu, nafsu makan dan minum menurun, puyuh ngorok,
            serta bersin-bersin yang diikuti keluarnya cairan dari hidung dan
            mata. Jika sudah parah akan terjadi pembengkakan dihidung, mata dan
            pipi. Penyakit snot biasanya terjadi pada musim pancaroba. Upaya
            pencegahan dilakukan dengan memberikan pakan yang cukup mutu dan
            jumlahnya, memberikan vitamin, antibiotik, dan selalu menjaga
            kondisi kandang agar selalu bersih, tidak terlalu lembab dan padat.
            Puyuh yang terkena penyakit snot dikarantina dan diberikan obat
            antibiotik, dosis dan cara pemberian sesuai dengan petunjuk pada
            kemasan.</p> 
            <p>2. Tetelo atau Newcastle Disease Penyakit tetelo atau
            Newcastle Disease disebabkan oleh virus ND dari famili Paramyxovirus
            yang menyerang saluran pernapasan. Gejala yang terlihat pada puyuh
            yang terkena penyakit ND diantaranya puyuh susah bernafas, puyuh
            ngorok, terlihat lesu, mengantuk, sayap turun, jalan mundur atau
            berputar-putar, serta kepala kepala menunduk kebawah. bahkan memutar
            hingga kebelakang. Hingga saat ini penyakit tetelo belum ditemukan
            obatnya. Peternak hanya bisa melakukan tindakan pencegahan, dengan
            menjaga kebersihan lingkungan kandang, peralatan, dan pakan,
            mengkarantina puyuh yang ambong sakit, serta memberikan vaksin ND
            sedini mungkin. Dosis yang diberikan sesuai dengan umur puyuh.</p> 
            <p>3. Berak Putih atau Pullorum Pullorum atau berak putih disebabkan oleh
            bakteri Salmonella pullorum. Gejala yang terlihat diantaranya adalah
            kotoran berwarna putih, puyuh sering bergerombol, terlihat
            mengantuk, nafsu makan menurun, sesak nafas, sayap lemah
            menggantung. Pengendalian penyakit berak putih hampir sama dengan
            pengendalian penyakit tetelo yaitu dengan memberikan antibiotik yang
            mengandung trimetoprim dan sulfadiazine.</p> 
            <p>4. Flu Burung atau Avian
            Influensa (Al) Penyakit flu burung disebabkan oleh virus Avian
            Influensa. Penyakit ini menyerang pernapasan dan sistem saraf. Flu
            burung biasanya terjadi secara mendadak sehingga dapat mengakibatkan
            kematian yang tinggi dalam waktu sehari. Gejala yang terlihat pada
            unggas yang terkena serangan penyakit ini antara lain pilek, hidung
            bengkak, keluar air mata, pial dan jengger berwarna biru. Hingga
            saat ini penyakit flu burung atau Avian Influensa belum ditemukan
            obatnya, dan hanya bisa dilakukan tindakan pencegahan yaitu dengan
            sanitasi, biosecurity yang ketat dan dilakukan vaksinasi.</p>
          </p>
